package Oct.oct11_Operator;

// = -> operator -> Assignment operator -> assign the value from right to the left
//Types -> 1. Unary, 2. Binary, 3. Ternary operator
/* 1. = Assignment operator
   2. +, -, *, /, % Arithmetic operators
   3. <, <=, >, >=, ==, != Relational operators

 */
public class Lab0 {
    public static void main(String[] args) {
       // Assignment operator
        int a = 10;
        System.out.println(a);
        // Arithmetic operator
        int b = 18;
        int c = 30;
        System.out.println(b + c);
        System.out.println(b - c);
        System.out.println(b * c);
        System.out.println(b / c);
        System.out.println(b % c);
    }
}
